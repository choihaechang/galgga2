package com.galgga.goods.vo;

public class GoodsVO {
	private int goods_no;
	private String goods_cate;
	private String goods_name;
	private String file_name;
	private String goods_brand;
	private int goods_price;
	private int goods_discount;
	private String goods_info;
	private String goods_addDate;
	
	public int getGoods_no() {
		return goods_no;
	}
	public void setGoods_no(int goods_no) {
		this.goods_no = goods_no;
	}
	public String getGoods_cate() {
		return goods_cate;
	}
	public void setGoods_cate(String goods_cate) {
		this.goods_cate = goods_cate;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getGoods_brand() {
		return goods_brand;
	}
	public void setGoods_brand(String goods_brand) {
		this.goods_brand = goods_brand;
	}
	public int getGoods_price() {
		return goods_price;
	}
	public void setGoods_price(int goods_price) {
		this.goods_price = goods_price;
	}
	public int getGoods_discount() {
		return goods_discount;
	}
	public void setGoods_discount(int goods_discount) {
		this.goods_discount = goods_discount;
	}
	public String getGoods_info() {
		return goods_info;
	}
	public void setGoods_info(String goods_info) {
		this.goods_info = goods_info;
	}
	public String getGoods_addDate() {
		return goods_addDate;
	}
	public void setGoods_addDate(String goods_addDate) {
		this.goods_addDate = goods_addDate;
	}
	
	
	
}
